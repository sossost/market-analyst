#!/usr/bin/env bash
#
# ETL Auto-Repair Script
#
# Called when an ETL job fails. Uses Claude Code CLI to analyze the error,
# fix the code, and create a PR. Does NOT merge — CEO decides.
#
# Environment variables (set by etl-repair.ts):
#   REPAIR_JOB_NAME      - Name of the failed ETL job
#   REPAIR_ERROR_LOG     - Error log from the failed job
#   REPAIR_RELATED_FILES - Comma-separated list of related file paths
#
# Exit codes:
#   0 - Repair attempted (check stdout for PR URL)
#   1 - Repair failed or skipped

set -euo pipefail

# --- Validate inputs ---
if [[ -z "${REPAIR_JOB_NAME:-}" ]]; then
  echo "ERROR: REPAIR_JOB_NAME is required" >&2
  exit 1
fi

if [[ -z "${REPAIR_ERROR_LOG:-}" ]]; then
  echo "ERROR: REPAIR_ERROR_LOG is required" >&2
  exit 1
fi

# --- Check Claude Code CLI ---
if ! command -v claude &> /dev/null; then
  echo "ERROR: Claude Code CLI not found" >&2
  exit 1
fi

# --- Setup ---
REPO_ROOT="$(cd "$(dirname "$0")/.." && pwd)"
TIMESTAMP="$(date +%Y%m%d-%H%M%S)"
PROMPT_TEMPLATE="${REPO_ROOT}/scripts/etl-repair-prompt.md"
ERROR_LOG_FILE="/tmp/etl-repair-error-${TIMESTAMP}.log"
REPAIR_SUCCESS=false
STASHED=""

if [[ ! -f "${PROMPT_TEMPLATE}" ]]; then
  echo "ERROR: Prompt template not found: ${PROMPT_TEMPLATE}" >&2
  exit 1
fi

# --- Cleanup trap ---
cleanup() {
  rm -f "${ERROR_LOG_FILE}"
  if [[ "${REPAIR_SUCCESS}" != "true" ]]; then
    git checkout main 2>/dev/null || true
    git branch -D "${BRANCH_NAME}" 2>/dev/null || true
  fi
  if [[ -n "${STASHED}" ]]; then
    # Only pop stash if we're back on main
    local current_branch
    current_branch="$(git branch --show-current 2>/dev/null || echo "")"
    if [[ "${current_branch}" == "main" ]]; then
      git stash pop 2>/dev/null || true
    fi
  fi
}
trap cleanup EXIT

# --- Sanitize job name for branch/git safety ---
JOB_SAFE="${REPAIR_JOB_NAME//[^a-zA-Z0-9_-]/_}"
BRANCH_NAME="auto-repair/${JOB_SAFE}-${TIMESTAMP}"

# --- Ensure clean working tree ---
cd "${REPO_ROOT}"
if ! git diff --quiet || ! git diff --cached --quiet; then
  echo "WARNING: Working tree is not clean. Stashing changes." >&2
  git stash push -m "etl-repair-stash-${TIMESTAMP}"
  STASHED="1"
fi

# --- Save error log to temp file (prevents bash injection) ---
printf '%s' "${REPAIR_ERROR_LOG}" > "${ERROR_LOG_FILE}"

# --- Build prompt ---
PROMPT=$(cat "${PROMPT_TEMPLATE}")
PROMPT="${PROMPT//\{\{JOB_NAME\}\}/${REPAIR_JOB_NAME}}"
PROMPT="${PROMPT//\{\{ERROR_LOG_FILE\}\}/${ERROR_LOG_FILE}}"
PROMPT="${PROMPT//\{\{RELATED_FILES\}\}/${REPAIR_RELATED_FILES:-}}"
PROMPT="${PROMPT//\{\{BRANCH_NAME\}\}/${BRANCH_NAME}}"

# --- Create branch ---
git checkout -b "${BRANCH_NAME}"

# --- Run Claude Code CLI ---
echo "Starting Claude Code auto-repair for: ${REPAIR_JOB_NAME}"

claude -p "${PROMPT}" \
  --allowedTools "Edit,Read,Grep,Glob,Write" \
  --output-format text \
  2>&1 || {
    echo "Claude Code CLI failed" >&2
    exit 1
  }

# --- Check if any changes were made ---
if git diff --quiet && git diff --cached --quiet; then
  echo "No changes made by Claude Code — nothing to repair"
  exit 0
fi

# --- Commit and push ---
git add src/etl/ src/db/
COMMIT_MSG="fix(etl): auto-repair ${REPAIR_JOB_NAME}

Automated fix generated by Claude Code CLI.
See PR description for error details."
git commit -m "${COMMIT_MSG}"

git push -u origin "${BRANCH_NAME}"

# --- Create PR ---
PR_BODY_FILE="/tmp/etl-repair-pr-body-${TIMESTAMP}.md"
cat > "${PR_BODY_FILE}" <<EOF
## 왜

ETL job \`${REPAIR_JOB_NAME}\` 실패 시 Claude Code CLI가 자동 생성한 수정 PR.

## 뭐가 달라지는가

자동 분석된 에러를 기반으로 코드 수정.

## 의사결정 필요

이 PR은 자동 생성되었습니다. **반드시 사람이 리뷰한 후 머지해 주세요.**

## 에러 로그

에러 로그 전문은 실행 환경(GitHub Actions 또는 서버 로그)에서 확인하세요.

## 리스크/제약

- 자동 생성된 수정이므로 반드시 수동 리뷰 필요
- 머지는 CEO 승인 후에만 가능

## 기술 요약

Claude Code CLI 자동 복구에 의해 생성된 PR.

## 전략비서 체크

- 골 정렬: SUPPORT (ETL 안정성)
- 무기 품질: 자동 생성 — 수동 리뷰 필요
- 무효 판정: 해당 없음
- 종합: HOLD (수동 리뷰 대기)

## 테스트

- [ ] TypeScript 타입 체크 통과
- [ ] 전체 테스트 통과
- [ ] 실 환경 확인 (해당 시)
EOF

PR_URL=$(gh pr create \
  --title "fix(etl): auto-repair ${REPAIR_JOB_NAME}" \
  --body-file "${PR_BODY_FILE}" 2>&1)

rm -f "${PR_BODY_FILE}"

echo "${PR_URL}"

# --- Return to main ---
REPAIR_SUCCESS=true
git checkout main

echo "Auto-repair complete. PR: ${PR_URL}"
